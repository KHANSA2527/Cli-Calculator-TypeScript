export function Add(num1, num2) {
    return num1 + num2;
}
export function Subtract(num1, num2) {
    return num1 - num2;
}
export function Mult(num1, num2) {
    return num1 * num2;
}
export function Division(num1, num2) {
    return num1 / num2;
}
export function Modulas(num1, num2) {
    return num1 % num2;
}
